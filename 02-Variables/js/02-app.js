//Inicializacion de variables con la palabra reservada let, lo recomendable es crearlas con let y no con var
let producto = 'Tablet';

//Reasignar el valor
producto = 'Monitor';
producto = 20;
producto = true;
producto = null;

console.log(producto);

//Inicialización de variable sin valor
let precio;

precio = 300;

console.log(precio);

